#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*p1;
	unsigned char	*p2;

	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	i = 0;
	while (i < n)
	{
		if (p1[i] != p2[i])
		{
			return (p1[i] - p2[i]);
		}
		i++;
	}
	return (0);
}
/*Compares the first n character of s1 to the first n characters of s2. 
Returns an integer greater than, equal to, or less than zero according to
the relationship between s1 and s2. 
Both strings are assumed to be n bytes long.*/
